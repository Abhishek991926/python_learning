# some tuple method 

#len() function it will give length of the tuple

tuple1=(1,2,3,4,5,10)
print(len(tuple1))

#max() it will return max value of the tuple 

print(max(tuple1))

# min() it will return min value of the tuple

print(min(tuple1))

# index() it will return index of the tuple elements :

print(tuple1.index(10))

# count method it will return frequency of the elements :

print(tuple1.count(10))
